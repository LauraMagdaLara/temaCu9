public class MathUtils {
    static int inmulteste(int a, int b){
        return  a*b;
    }
    static int inmulteste(int a, int b,int c){
        return  a*b*c;
    }
    static double inmulteste(int a, double b){
        return  a*b;
    }
    static double inmulteste(double a, int b){
        return  a*b;
    }
}
